Od 1989 roku do chwili obecnej ów zakład piekarniczy a dzisiaj także zakład piekarniczo- cukierniczy kierowany jest przez Janusza i Małgorzatę Romanowskich. Od 1989 roku firma funkcjonowała pod nazwą Piekarstwo- Cukiernictwo aż do roku 2006, kiedy firma przekształciła się w spółkę jawną i uzyskała nazwę funkcjonującą po dziś dzień Piekarstwo - Cukiernictwo "Jaś i Małgosia". 
Tradycja firmy "Jaś i Małgosia" ma wymiar podwójny, ponieważ nie tylko Janusz Romanowski wywodzi się z rodziny cukierniczo- piekarniczej ale również i Małgorzata Romanowska, która jest córką Kazimierza Wawrzyniaka, mistrza cukiernictwa. 
Rodzice obecnych właścicieli firmy zadbali o to, aby każdy z nich posiadał odpowiednią wiedzę z zakresu piekarstwa i cukiernictwa, ponieważ od dziecka uczestniczyli w funkcjonowaniu zakładu cukierniczego i piekarniczego oraz byli nadzieją rodziców na kontynowanie rodzinnego biznesu. Zarówno Małgorzata jak i Janusz Romanowski posiadają dyplomy mistrza piekarstwa i cukiernictwa.

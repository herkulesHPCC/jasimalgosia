					<div class="history-left-grid" style="float: left; width: 65%; text-align:justify;"><p class="ever"><br>"Piekarstwo-Cukiernictwo Jaś i Małgosia"
					Piekarstwo - Cukiernictwo Jaś i Małgosia jest firmą rodzinną z wieloletnią tradycją. W 1925 roku Franciszek Romanowski- prekursor tradycji piekarstwa w rodzinie Romanowskich zakończył nauki zawodu piekarza w piekarni w Szadku koło Łodzi. Następnie otworzył małą piekarenkę w Kwiatkowicach koło Łodzi. 
					Właśnie rok 1925 uważa się za założenie firmy Piekarstwo- Cukiernictwo Jaś i Małgosia. Po 8 latach działania małej piekarni w Szadku, bracia Franciszek oraz Ignacy przyjechali do Łodzi z myślą znalezienia nowej siedziby piekarni. Uwagę braci zwrócił zakład piekarniczy przy ulicy Bohaterów, obecnie przy ulicy Strykowskiej 79/81w Łodzi. W późniejszym czasie drogi rodzinne braci rozeszły się. Ignacy Romanowski wraz z żoną założyli swoją firmę piekarniczą, natomiast Franciszek Romanowski postanowił kontynuować działalność piekarni przy ulicy Strykowskiej 79/81.

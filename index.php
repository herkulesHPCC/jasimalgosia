<p class="ever" style=" float: left; text-align:justify;margin-top: 40px;">
"Jaś i Małgosia" to nie tylko zakład produkcyjny ale również sieć sklepów firmowych w całej Łodzi. Sklepy zlokalizowane są na terenie wszystkich dzielnic Łodzi, począwszy od punktu sprzedaży przy zakładzie produkcyjnym na ulicy Strykowskiej aż po najdalsze dzielnice Łodzi np. Retkinia, Widzew.
Piekarstwo - Cukiernictwo "Jaś i Małgosia" proponuje swoim klientom obszerną gamę produktów piekarniczych jak i cukierniczych produkowanych na podstawie starych receptur, które smakiem przypominają tradycyjne polskie wyroby, o których w dzisiejszych czasach jest bardzo trudno. 
Do produktów piekarniczych, które można nabyć w naszych sklepach należy zarówno pieczywo tradycyjne np. chleb biały, bułki kajzerki, bułki z przedziałkiem jak i pieczywo gatunkowe np. chleb jarmarczny, orkiszowy, z żurawiną. 
Firma proponuje swoim klientom również wyroby cukiernicze takie jak: ciasteczka, ciasta np. ciasto marchewkowe, bułki drożdżowe, pączki o różnych smakach a także torty na specjalne okazje. Każdy klient ma możliwość zamówienia w sklepie firmowym tortu o wymarzonym smaku i kształcie. Właściciele mają na uwadze preferencje klienta, dlatego w naszej firmie klient sam może zaprojektować tort, który później cukiernia wykona. 
Przedsiębiorstwo oprócz wyrobów cukierniczych i piekarniczych oferuje również różnego rodzaju kanapki oraz przekąski typu pizza bolońska, krokiety z kapustą, bigosówki, parówki w cieście. Tego typu wyroby są zdrowym, pożywnym posiłkiem i produkowane są szczególnie z myślą o zapracowanych rodzicach, którzy niekoniecznie mają czas zrobić kanapkę do pracy czy dziecku do szkoły. 
Właściciele firmy w sposób szczególny dbają o wysoką jakość swoich wyrobów, dlatego w 2000 r. zaczęli wprowadzać system HACCP, który zapewnia bezpieczeństwo wyrobów i ich odpowiednią jakość. Co więcej urządzenia stosowane podczas procesu produkcji są najnowocześniejszymi na rynku maszynami, które spełniają wymogi systemu HACCP. 
W 2007 roku wysiłek wszystkich pracowników i właścicieli został doceniony, ponieważ przedsiębiorstwo zdobyło nagrodę Grand Prix- Złoty Promień i tytuł "Mistera chleba 2007" w konkursie organizowanym przez Express Ilustrowany. Zaś w 2009 roku firma osiągnęła pierwsze miejsce w konkursie na najlepszego pączka, który był również zorganizowany przez Express Ilustrowany. 
